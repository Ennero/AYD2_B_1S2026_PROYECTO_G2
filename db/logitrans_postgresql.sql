    CARGO_NAME VARCHAR(100) NOT NULL UNIQUE,
    REQUIRES_REFRIGERATION BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE CONTRACTS (
    CONTRACT_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CONTRACT_NUMBER VARCHAR(40) NOT NULL UNIQUE DEFAULT ('CONT-' || LPAD(nextval('SEQ_CONTRACT_NUMBER')::TEXT, 5, '0')),
    CLIENT_ID INTEGER NOT NULL REFERENCES CLIENTS (CLIENT_ID),
    STATUS CONTRACT_STATUS NOT NULL DEFAULT 'BORRADOR',
    START_DATE DATE NOT NULL DEFAULT CURRENT_DATE,
    END_DATE DATE NOT NULL DEFAULT ((CURRENT_DATE + INTERVAL '1 year')::DATE),
    ACCEPTED_AT TIMESTAMPTZ,
    CREDIT_LIMIT NUMERIC(14, 2) CHECK (CREDIT_LIMIT >= 0),
    CURRENCY_CODE CURRENCY_CODE NOT NULL DEFAULT 'GTQ',
    EXCHANGE_RATE_FROM_USD NUMERIC(14, 6) NOT NULL DEFAULT 1.000000 CHECK (EXCHANGE_RATE_FROM_USD > 0),
    TAX_RATE NUMERIC(5, 4) NOT NULL DEFAULT 0.1200 CHECK (TAX_RATE >= 0 AND TAX_RATE <= 1),
    PAYMENT_TERM_DAYS SMALLINT NOT NULL CHECK (PAYMENT_TERM_DAYS > 0),
    DISCOUNT_PERCENTAGE NUMERIC(5, 2) NOT NULL DEFAULT 0 CHECK (
        DISCOUNT_PERCENTAGE >= 0 AND DISCOUNT_PERCENTAGE <= 100
