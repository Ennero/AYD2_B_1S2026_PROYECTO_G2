CREATE OR REPLACE VIEW V_CLIENT_BALANCES AS
WITH ACTIVE_CONTRACT AS (
    SELECT
        CLIENT_ID,
        CREDIT_LIMIT
    FROM CONTRACTS
    WHERE STATUS = 'VIGENTE'
),
INVOICE_TOTALS AS (
    SELECT
        C.CLIENT_ID,
        COALESCE(SUM(I.TOTAL_AMOUNT) FILTER (WHERE I.STATUS <> 'RECHAZADA'), 0) AS INVOICED_TOTAL,
        COUNT(*) FILTER (
            WHERE I.STATUS <> 'PAGADA'
              AND I.STATUS <> 'RECHAZADA'
              AND I.DUE_DATE < CURRENT_DATE
        ) AS OVERDUE_INVOICES
    FROM CLIENTS C
    LEFT JOIN INVOICES I
        ON I.CLIENT_ID = C.CLIENT_ID
    GROUP BY C.CLIENT_ID
),
PAYMENTS_TOTALS AS (
    SELECT
        C.CLIENT_ID,
        COALESCE(SUM(P.AMOUNT) FILTER (WHERE P.STATUS = 'APROBADO'), 0) AS PAID_TOTAL
    FROM CLIENTS C
    LEFT JOIN INVOICES I
        ON I.CLIENT_ID = C.CLIENT_ID
    LEFT JOIN PAYMENTS P
        ON P.INVOICE_ID = I.INVOICE_ID
    GROUP BY C.CLIENT_ID
)
SELECT
    C.CLIENT_ID,
    C.CLIENT_CODE,
    C.LEGAL_NAME,
    COALESCE(AC.CREDIT_LIMIT, 0) AS CREDIT_LIMIT,
    COALESCE(IT.INVOICED_TOTAL, 0) - COALESCE(PT.PAID_TOTAL, 0) AS OUTSTANDING_BALANCE,
    COALESCE(AC.CREDIT_LIMIT, 0) - (COALESCE(IT.INVOICED_TOTAL, 0) - COALESCE(PT.PAID_TOTAL, 0)) AS AVAILABLE_CREDIT,
    COALESCE(IT.OVERDUE_INVOICES, 0) AS OVERDUE_INVOICES,
    (
        C.IS_BLOCKED
        OR COALESCE(IT.OVERDUE_INVOICES, 0) > 0
        OR COALESCE(AC.CREDIT_LIMIT, 0) - (COALESCE(IT.INVOICED_TOTAL, 0) - COALESCE(PT.PAID_TOTAL, 0)) < 0
    ) AS SHOULD_BLOCK_ORDERS
FROM CLIENTS C
LEFT JOIN ACTIVE_CONTRACT AC
    ON AC.CLIENT_ID = C.CLIENT_ID
LEFT JOIN INVOICE_TOTALS IT
    ON IT.CLIENT_ID = C.CLIENT_ID
LEFT JOIN PAYMENTS_TOTALS PT
    ON PT.CLIENT_ID = C.CLIENT_ID;

CREATE OR REPLACE VIEW V_DAILY_OPERATIONS AS
SELECT
    B.BRANCH_ID,
    B.BRANCH_NAME,
    DATE(O.REQUESTED_AT) AS OPERATION_DATE,
    COUNT(*) AS TOTAL_ORDERS,
    COUNT(*) FILTER (WHERE O.STATUS = 'ENTREGADA') AS DELIVERED_ORDERS,
    COALESCE(SUM(I.TOTAL_AMOUNT) FILTER (WHERE I.STATUS <> 'RECHAZADA'), 0) AS INVOICED_TOTAL,
    COUNT(*) FILTER (WHERE I.STATUS = 'PAGADA') AS PAID_INVOICES
FROM BRANCHES B
LEFT JOIN ORDERS O
    ON O.BRANCH_ID = B.BRANCH_ID
LEFT JOIN INVOICES I
    ON I.ORDER_ID = O.ORDER_ID
GROUP BY B.BRANCH_ID, B.BRANCH_NAME, DATE(O.REQUESTED_AT);

CREATE OR REPLACE VIEW V_CONTRACT_PROFITABILITY AS
SELECT
    CT.CONTRACT_ID,
    CT.CONTRACT_NUMBER,
    CT.CLIENT_ID,
    COALESCE(SUM(I.TOTAL_AMOUNT) FILTER (WHERE I.STATUS <> 'RECHAZADA'), 0) AS REVENUE_AMOUNT,
    COALESCE(SUM(O.FUEL_COST + O.VIATICS_COST + O.MAINTENANCE_COST), 0) AS OPERATING_COST_AMOUNT,
    COALESCE(SUM(I.TOTAL_AMOUNT) FILTER (WHERE I.STATUS <> 'RECHAZADA'), 0)
        - COALESCE(SUM(O.FUEL_COST + O.VIATICS_COST + O.MAINTENANCE_COST), 0) AS GROSS_MARGIN_AMOUNT
FROM CONTRACTS CT
LEFT JOIN ORDERS O
    ON O.CONTRACT_ID = CT.CONTRACT_ID
LEFT JOIN INVOICES I
    ON I.ORDER_ID = O.ORDER_ID
GROUP BY CT.CONTRACT_ID, CT.CONTRACT_NUMBER, CT.CLIENT_ID;

