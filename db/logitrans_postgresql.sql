CREATE OR REPLACE FUNCTION VALIDATE_PAYMENT_AMOUNT()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    V_TOTAL NUMERIC(14, 2);
    V_APPROVED_PAYMENT_ID INTEGER;
    V_INVOICE_STATUS INVOICE_STATUS;
    V_INVOICE_CURRENCY CURRENCY_CODE;
BEGIN
    SELECT TOTAL_AMOUNT, STATUS, CURRENCY_CODE
      INTO V_TOTAL, V_INVOICE_STATUS, V_INVOICE_CURRENCY
    FROM INVOICES
    WHERE INVOICE_ID = NEW.INVOICE_ID;

    IF V_TOTAL IS NULL THEN
        RAISE EXCEPTION 'La factura % no existe.', NEW.INVOICE_ID;
    END IF;

    IF V_INVOICE_STATUS = 'PAGADA' THEN
        RAISE EXCEPTION 'La factura % ya fue pagada.', NEW.INVOICE_ID;
    END IF;

        SELECT PAYMENT_ID
            INTO V_APPROVED_PAYMENT_ID
        FROM PAYMENTS
        WHERE INVOICE_ID = NEW.INVOICE_ID
            AND STATUS = 'APROBADO'
            AND PAYMENT_ID <> NEW.PAYMENT_ID
        LIMIT 1;

    IF V_APPROVED_PAYMENT_ID IS NOT NULL THEN
        RAISE EXCEPTION 'La factura % ya tiene un pago aprobado.', NEW.INVOICE_ID;
    END IF;

    NEW.CURRENCY_CODE = V_INVOICE_CURRENCY;

    IF NEW.AMOUNT <> V_TOTAL THEN
        RAISE EXCEPTION 'El pago % debe coincidir exactamente con el total de la factura %.', NEW.AMOUNT, V_TOTAL;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION SYNC_INVOICE_PAYMENT()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.STATUS = 'APROBADO' THEN
        UPDATE INVOICES
        SET STATUS = 'PAGADA'
        WHERE INVOICE_ID = NEW.INVOICE_ID
          AND STATUS = 'CERTIFICADA';
    END IF;

    RETURN NEW;
END;
$$;

