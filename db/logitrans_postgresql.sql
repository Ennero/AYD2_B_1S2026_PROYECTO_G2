CREATE OR REPLACE FUNCTION VALIDATE_TRANSPORT_UNIT()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    V_MIN NUMERIC(6, 2);
    V_MAX NUMERIC(6, 2);
BEGIN
    SELECT MIN_CAPACITY_TON, MAX_CAPACITY_TON
      INTO V_MIN, V_MAX
    FROM VEHICLE_TYPES
    WHERE VEHICLE_TYPE_ID = NEW.VEHICLE_TYPE_ID;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'VEHICLE_TYPE_ID % no existe.', NEW.VEHICLE_TYPE_ID;
    END IF;

    IF NEW.CAPACITY_TON < V_MIN OR (V_MAX IS NOT NULL AND NEW.CAPACITY_TON > V_MAX) THEN
        RAISE EXCEPTION 'La capacidad % no coincide con el rango configurado del tipo de vehiculo.', NEW.CAPACITY_TON;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION VALIDATE_ORDER_ASSIGNMENT()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    V_CAPACITY NUMERIC(6, 2);
    V_HAS_REFRIGERATION BOOLEAN;
    V_REQUIRES_REFRIGERATION BOOLEAN;
    V_BRANCH_ID INTEGER;
    V_VEHICLE_TYPE_ID INTEGER;
    V_RATE_ID INTEGER;
    V_BASE_RATE NUMERIC(12, 2);
    V_FINAL_RATE NUMERIC(12, 2);
    V_DISTANCE NUMERIC(10, 2);
    V_PILOT_LICENSE_EXPIRATION DATE;
    V_VEHICLE_DOCUMENT_EXPIRATION DATE;
    V_CONTRACT_CURRENCY CURRENCY_CODE;
    V_CONTRACT_EXCHANGE_RATE NUMERIC(14, 6);
    V_CONTRACT_TAX_RATE NUMERIC(5, 4);
BEGIN
    SELECT CURRENCY_CODE, EXCHANGE_RATE_FROM_USD, TAX_RATE
      INTO V_CONTRACT_CURRENCY, V_CONTRACT_EXCHANGE_RATE, V_CONTRACT_TAX_RATE
    FROM CONTRACTS
    WHERE CONTRACT_ID = NEW.CONTRACT_ID;

    IF FOUND THEN
        NEW.CURRENCY_CODE = V_CONTRACT_CURRENCY;
        NEW.EXCHANGE_RATE_FROM_USD = V_CONTRACT_EXCHANGE_RATE;
        NEW.TAX_RATE = V_CONTRACT_TAX_RATE;
    END IF;

    IF NEW.ORIGIN IS NULL OR NEW.ORIGIN = '' THEN
        NEW.ORIGIN = NEW.PICKUP_ADDRESS;
    END IF;

    IF NEW.DESTINATION IS NULL OR NEW.DESTINATION = '' THEN
        NEW.DESTINATION = NEW.DELIVERY_ADDRESS;
    END IF;

    IF NEW.UNIT_ID IS NULL THEN
                IF NEW.CONTRACT_ROUTE_ID IS NOT NULL AND NEW.DISTANCE_KM = 0 THEN
                        SELECT R.DISTANCE_KM
                            INTO V_DISTANCE
                        FROM CONTRACT_ROUTES CR
                        JOIN ROUTES R
                            ON R.ROUTE_ID = CR.ROUTE_ID
                        WHERE CR.CONTRACT_ROUTE_ID = NEW.CONTRACT_ROUTE_ID;

                        IF FOUND THEN
                                NEW.DISTANCE_KM = V_DISTANCE;
                        END IF;
                END IF;

                IF NEW.SUBTOTAL_AMOUNT = 0 AND NEW.DISTANCE_KM > 0 AND NEW.FINAL_RATE_PER_KM > 0 THEN
                        NEW.SUBTOTAL_AMOUNT = ROUND(NEW.DISTANCE_KM * NEW.FINAL_RATE_PER_KM, 2);
                    NEW.TAX_AMOUNT = ROUND(NEW.SUBTOTAL_AMOUNT * NEW.TAX_RATE, 2);
                        NEW.TOTAL_AMOUNT = NEW.SUBTOTAL_AMOUNT + NEW.TAX_AMOUNT;
                END IF;

                RETURN NEW;
    END IF;

    SELECT CAPACITY_TON, HAS_REFRIGERATION, BRANCH_ID, VEHICLE_TYPE_ID, PILOT_LICENSE_EXPIRATION, VEHICLE_DOCUMENT_EXPIRATION
        INTO V_CAPACITY, V_HAS_REFRIGERATION, V_BRANCH_ID, V_VEHICLE_TYPE_ID, V_PILOT_LICENSE_EXPIRATION, V_VEHICLE_DOCUMENT_EXPIRATION
    FROM TRANSPORT_UNITS
    WHERE UNIT_ID = NEW.UNIT_ID;

    SELECT REQUIRES_REFRIGERATION
      INTO V_REQUIRES_REFRIGERATION
    FROM CARGO_TYPES
    WHERE CARGO_TYPE_ID = NEW.CARGO_TYPE_ID;

    IF NEW.DECLARED_WEIGHT_TON > (V_CAPACITY + 0.02) THEN
        RAISE EXCEPTION 'El peso declarado % supera la capacidad de la unidad %.', NEW.DECLARED_WEIGHT_TON, V_CAPACITY;
    END IF;

    IF NEW.LOADED_WEIGHT_TON IS NOT NULL AND NEW.LOADED_WEIGHT_TON > (V_CAPACITY + 0.02) THEN
        RAISE EXCEPTION 'El peso cargado % supera la capacidad de la unidad %.', NEW.LOADED_WEIGHT_TON, V_CAPACITY;
    END IF;

    IF NEW.LOADED_WEIGHT_TON IS NOT NULL AND ABS(NEW.LOADED_WEIGHT_TON - NEW.DECLARED_WEIGHT_TON) > 0.02 THEN
        RAISE EXCEPTION 'El peso cargado % difiere del declarado % por mas de 0.02.', NEW.LOADED_WEIGHT_TON, NEW.DECLARED_WEIGHT_TON;
    END IF;

    IF V_REQUIRES_REFRIGERATION AND NOT V_HAS_REFRIGERATION THEN
        RAISE EXCEPTION 'La unidad asignada no cumple con el requisito de refrigeracion.';
    END IF;

    IF V_PILOT_LICENSE_EXPIRATION < CURRENT_DATE THEN
        RAISE EXCEPTION 'La licencia del piloto asociado a la unidad esta vencida.';
    END IF;

    IF V_VEHICLE_DOCUMENT_EXPIRATION < CURRENT_DATE THEN
        RAISE EXCEPTION 'La documentacion del vehiculo asignado esta vencida.';
    END IF;

    IF NEW.BRANCH_ID IS NULL THEN
        NEW.BRANCH_ID = V_BRANCH_ID;
    END IF;

    IF NEW.CONTRACT_RATE_ID IS NULL THEN
        SELECT CONTRACT_RATE_ID, BASE_RATE_PER_KM, FINAL_RATE_PER_KM
          INTO V_RATE_ID, V_BASE_RATE, V_FINAL_RATE
        FROM CONTRACT_RATES
        WHERE CONTRACT_ID = NEW.CONTRACT_ID
          AND VEHICLE_TYPE_ID = V_VEHICLE_TYPE_ID
        LIMIT 1;

        IF FOUND THEN
            NEW.CONTRACT_RATE_ID = V_RATE_ID;
            IF NEW.BASE_RATE_PER_KM = 0 THEN
                NEW.BASE_RATE_PER_KM = V_BASE_RATE;
            END IF;
            IF NEW.FINAL_RATE_PER_KM = 0 THEN
                NEW.FINAL_RATE_PER_KM = V_FINAL_RATE;
            END IF;
        END IF;
    END IF;

    IF NEW.CONTRACT_ROUTE_ID IS NOT NULL AND NEW.DISTANCE_KM = 0 THEN
        SELECT R.DISTANCE_KM
          INTO V_DISTANCE
        FROM CONTRACT_ROUTES CR
        JOIN ROUTES R
          ON R.ROUTE_ID = CR.ROUTE_ID
        WHERE CR.CONTRACT_ROUTE_ID = NEW.CONTRACT_ROUTE_ID;

        IF FOUND THEN
            NEW.DISTANCE_KM = V_DISTANCE;
        END IF;
    END IF;

    IF NEW.SUBTOTAL_AMOUNT = 0 AND NEW.DISTANCE_KM > 0 AND NEW.FINAL_RATE_PER_KM > 0 THEN
        NEW.SUBTOTAL_AMOUNT = ROUND(NEW.DISTANCE_KM * NEW.FINAL_RATE_PER_KM, 2);
        NEW.TAX_AMOUNT = ROUND(NEW.SUBTOTAL_AMOUNT * NEW.TAX_RATE, 2);
        NEW.TOTAL_AMOUNT = NEW.SUBTOTAL_AMOUNT + NEW.TAX_AMOUNT;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION VALIDATE_ORDER_COMMERCIAL_RULES()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE V_CLIENT_ID INTEGER;
    V_CONTRACT_STATUS CONTRACT_STATUS;
    V_START_DATE DATE;
    V_END_DATE DATE;
    V_CONTRACT_CREDIT_LIMIT NUMERIC(14, 2);
    V_IS_BLOCKED BOOLEAN;
    V_BLOCK_REASON TEXT;
    V_INVOICED_TOTAL NUMERIC(14, 2);
    V_PAID_TOTAL NUMERIC(14, 2);
    V_OVERDUE_INVOICES INTEGER;
BEGIN
    SELECT
        CT.CLIENT_ID,
        CT.STATUS,
        CT.START_DATE,
        CT.END_DATE,
        CT.CREDIT_LIMIT,
        C.IS_BLOCKED,
        C.BLOCK_REASON
    INTO
        V_CLIENT_ID,
        V_CONTRACT_STATUS,
        V_START_DATE,
        V_END_DATE,
        V_CONTRACT_CREDIT_LIMIT,
        V_IS_BLOCKED,
        V_BLOCK_REASON
    FROM CONTRACTS CT
    JOIN CLIENTS C
      ON C.CLIENT_ID = CT.CLIENT_ID
    WHERE CT.CONTRACT_ID = NEW.CONTRACT_ID;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'El contrato % no existe.', NEW.CONTRACT_ID;
    END IF;

    IF V_CONTRACT_STATUS <> 'VIGENTE' THEN
        RAISE EXCEPTION 'No se puede crear la orden porque el contrato no esta vigente.';
    END IF;

    IF CURRENT_DATE < V_START_DATE OR CURRENT_DATE > V_END_DATE THEN
        RAISE EXCEPTION 'No se puede crear la orden porque el contrato esta fuera de vigencia.';
    END IF;

    IF V_IS_BLOCKED THEN
        RAISE EXCEPTION 'No se puede crear la orden porque el cliente esta bloqueado: %', COALESCE(V_BLOCK_REASON, 'sin motivo especificado');
    END IF;

    SELECT
        COALESCE(SUM(I.TOTAL_AMOUNT) FILTER (WHERE I.STATUS <> 'RECHAZADA'), 0),
        COALESCE(SUM(P.AMOUNT) FILTER (WHERE P.STATUS = 'APROBADO'), 0),
        COUNT(*) FILTER (
            WHERE I.STATUS <> 'PAGADA'
              AND I.STATUS <> 'RECHAZADA'
              AND I.DUE_DATE < CURRENT_DATE
        )
    INTO
        V_INVOICED_TOTAL,
        V_PAID_TOTAL,
        V_OVERDUE_INVOICES
    FROM CONTRACTS CT
    LEFT JOIN ORDERS O
      ON O.CONTRACT_ID = CT.CONTRACT_ID
    LEFT JOIN INVOICES I
      ON I.ORDER_ID = O.ORDER_ID
    LEFT JOIN PAYMENTS P
      ON P.INVOICE_ID = I.INVOICE_ID
    WHERE CT.CLIENT_ID = V_CLIENT_ID;

    IF V_OVERDUE_INVOICES > 0 THEN
        RAISE EXCEPTION 'No se puede crear la orden porque el cliente tiene facturas vencidas.';
    END IF;

    IF V_CONTRACT_CREDIT_LIMIT - (V_INVOICED_TOTAL - V_PAID_TOTAL) < 0 THEN
        RAISE EXCEPTION 'No se puede crear la orden porque el cliente excedio su limite de credito.';
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION POPULATE_INVOICE_FROM_ORDER()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE V_CLIENT_ID INTEGER;
    V_CLIENT_NAME VARCHAR(180);
    V_CLIENT_NIT VARCHAR(20);
    V_CLIENT_ADDRESS TEXT;
    V_ORDER_NUMBER VARCHAR(40);
    V_TOTAL NUMERIC(14, 2);
    V_SUBTOTAL NUMERIC(14, 2);
    V_TAX NUMERIC(14, 2);
    V_TAX_RATE NUMERIC(5, 4);
    V_TERM SMALLINT;
    V_CURRENCY_CODE CURRENCY_CODE;
    V_EXCHANGE_RATE NUMERIC(14, 6);
BEGIN
    SELECT
        C.CLIENT_ID,
        C.LEGAL_NAME,
        C.NIT,
        C.TAX_ADDRESS,
        O.ORDER_NUMBER,
        O.TOTAL_AMOUNT,
        O.SUBTOTAL_AMOUNT,
        O.TAX_AMOUNT,
                O.TAX_RATE,
                O.CURRENCY_CODE,
                O.EXCHANGE_RATE_FROM_USD,
        CT.PAYMENT_TERM_DAYS
      INTO
        V_CLIENT_ID,
        V_CLIENT_NAME,
        V_CLIENT_NIT,
        V_CLIENT_ADDRESS,
        V_ORDER_NUMBER,
        V_TOTAL,
        V_SUBTOTAL,
        V_TAX,
        V_TAX_RATE,
        V_CURRENCY_CODE,
        V_EXCHANGE_RATE,
        V_TERM
    FROM ORDERS O
    JOIN CONTRACTS CT
      ON CT.CONTRACT_ID = O.CONTRACT_ID
    JOIN CLIENTS C
      ON C.CLIENT_ID = CT.CLIENT_ID
    WHERE O.ORDER_ID = NEW.ORDER_ID;

    IF NEW.CLIENT_ID IS NULL THEN
        NEW.CLIENT_ID = V_CLIENT_ID;
    END IF;

    IF NEW.CLIENT_NAME IS NULL OR NEW.CLIENT_NAME = '' THEN
        NEW.CLIENT_NAME = V_CLIENT_NAME;
    END IF;

    IF NEW.CLIENT_NIT IS NULL OR NEW.CLIENT_NIT = '' THEN
        NEW.CLIENT_NIT = V_CLIENT_NIT;
    END IF;

    IF NEW.CLIENT_ADDRESS IS NULL OR NEW.CLIENT_ADDRESS = '' THEN
        NEW.CLIENT_ADDRESS = V_CLIENT_ADDRESS;
    END IF;

    IF NEW.SERVICE_DESCRIPTION IS NULL THEN
        NEW.SERVICE_DESCRIPTION = '';
    END IF;

    NEW.CURRENCY_CODE = V_CURRENCY_CODE;
    NEW.EXCHANGE_RATE_FROM_USD = V_EXCHANGE_RATE;
    NEW.TAX_RATE = V_TAX_RATE;

    IF COALESCE(NEW.SUBTOTAL_AMOUNT, 0) = 0 THEN
        NEW.SUBTOTAL_AMOUNT = V_SUBTOTAL;
    END IF;

    IF COALESCE(NEW.TAX_AMOUNT, 0) = 0 THEN
        NEW.TAX_AMOUNT = V_TAX;
    END IF;

    IF COALESCE(NEW.TOTAL_AMOUNT, 0) = 0 THEN
        NEW.TOTAL_AMOUNT = V_TOTAL;
    END IF;

    IF NEW.DUE_DATE IS NULL THEN
        NEW.DUE_DATE = (COALESCE(NEW.ISSUE_DATE, NOW())::DATE + COALESCE(V_TERM, 30));
    END IF;

    RETURN NEW;
END;
$$;

